package com.insigma.siis.local.business.entity;

public class ComprehensiveSet {
	
	private String id;
	private String unit;
	private String tableType;
	private String teamResponsibility;
	private String generalEvaluation;
	private String optimizeDirection;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getTableType() {
		return tableType;
	}
	public void setTableType(String tableType) {
		this.tableType = tableType;
	}
	public String getTeamResponsibility() {
		return teamResponsibility;
	}
	public void setTeamResponsibility(String teamResponsibility) {
		this.teamResponsibility = teamResponsibility;
	}
	public String getGeneralEvaluation() {
		return generalEvaluation;
	}
	public void setGeneralEvaluation(String generalEvaluation) {
		this.generalEvaluation = generalEvaluation;
	}
	public String getOptimizeDirection() {
		return optimizeDirection;
	}
	public void setOptimizeDirection(String optimizeDirection) {
		this.optimizeDirection = optimizeDirection;
	}
	
	
	
}

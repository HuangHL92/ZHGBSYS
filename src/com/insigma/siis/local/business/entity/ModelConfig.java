package com.insigma.siis.local.business.entity;

public class ModelConfig {
	private String id;
	private String expertise;//专长
	private String region;//地域
	private String ability;//能力
	private String experience;//经验
	private String undergo;//经历
	private String source;//来源
	private String tenuresystem;//任期制
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getExpertise() {
		return expertise;
	}
	public void setExpertise(String expertise) {
		this.expertise = expertise;
	}
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public String getAbility() {
		return ability;
	}
	public void setAbility(String ability) {
		this.ability = ability;
	}
	public String getExperience() {
		return experience;
	}
	public void setExperience(String experience) {
		this.experience = experience;
	}
	public String getUndergo() {
		return undergo;
	}
	public void setUndergo(String undergo) {
		this.undergo = undergo;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getTenuresystem() {
		return tenuresystem;
	}
	public void setTenuresystem(String tenuresystem) {
		this.tenuresystem = tenuresystem;
	}

}
